package eg.mos.sportify.domain.enums;


/**
 * Enum representing the roles a participant can have in a competition.

 * This enum defines the possible roles for participants:
 * - {@link #PLAYER}: Represents a participant who plays in the competition.
 * - {@link #ORGANIZER}: Represents a participant who organizes the competition.
 */
public enum PlayerRole {
    PLAYER, ORGANIZER
}
